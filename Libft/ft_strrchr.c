#include "libft.h"

char	*strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s != 0)
	{
		s ++;
		i ++;
	}
	while (*s != c && i > 0)
	{
		if (*s == c)
			return ((char *)s);
		s --;
		i --;
	}
	return (NULL);
}
